// == igual a
// === valor igual e igual
// != não é igual
// !== não é igual ou não igual
// > maior que
// < menor que
// >= maior que ou igual a
// <= menor que ou igual a

var a = 3;
var b = "3";

console.log(a == b);

console.log(a === b);

console.log(a != b);

console.log(a !== b);

var b = 4;

console.log(a > b);

console.log(a < b);

console.log(a >= b);

